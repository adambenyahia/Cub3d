#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*c;

	if (!lst)
		return (0);
	i = 1;
	c = lst;
	while (c->next)
	{
		i++;
		c = c->next;
	}
	return (i);
}
